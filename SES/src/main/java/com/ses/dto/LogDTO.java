package com.ses.dto;

public class LogDTO {
	private String M_ID = "";
	private String S_NAME = "";
	private String L_SITE = "";
	private String L_ACTIVITY = "";
	private int L_YEAR = 0;
	private int L_MONTH = 0;
	private int L_DAY = 0;
	private int L_HOUR = 0;
	private int L_MINUTE = 0;

	public LogDTO(String m_ID, String s_NAME, String l_SITE, String l_ACTIVITY, int l_YEAR, int l_MONTH, int l_DAY,
			int l_HOUR, int l_MINUTE) {
		super();
		M_ID = m_ID;
		S_NAME = s_NAME;
		L_SITE = l_SITE;
		L_ACTIVITY = l_ACTIVITY;
		L_YEAR = l_YEAR;
		L_MONTH = l_MONTH;
		L_DAY = l_DAY;
		L_HOUR = l_HOUR;
		L_MINUTE = l_MINUTE;
	}

	public String getM_ID() {
		return M_ID;
	}

	public void setM_ID(String m_ID) {
		M_ID = m_ID;
	}

	public String getS_NAME() {
		return S_NAME;
	}

	public void setS_NAME(String s_NAME) {
		S_NAME = s_NAME;
	}

	public String getL_SITE() {
		return L_SITE;
	}

	public void setL_SITE(String l_SITE) {
		L_SITE = l_SITE;
	}

	public String getL_ACTIVITY() {
		return L_ACTIVITY;
	}

	public void setL_ACTIVITY(String l_ACTIVITY) {
		L_ACTIVITY = l_ACTIVITY;
	}

	public int getL_YEAR() {
		return L_YEAR;
	}

	public void setL_YEAR(int l_YEAR) {
		L_YEAR = l_YEAR;
	}

	public int getL_MONTH() {
		return L_MONTH;
	}

	public void setL_MONTH(int l_MONTH) {
		L_MONTH = l_MONTH;
	}

	public int getL_DAY() {
		return L_DAY;
	}

	public void setL_DAY(int l_DAY) {
		L_DAY = l_DAY;
	}

	public int getL_HOUR() {
		return L_HOUR;
	}

	public void setL_HOUR(int l_HOUR) {
		L_HOUR = l_HOUR;
	}

	public int getL_MINUTE() {
		return L_MINUTE;
	}

	public void setL_MINUTE(int l_MINUTE) {
        L_MINUTE = l_MINUTE;
    }
}
